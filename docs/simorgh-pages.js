const capitalizeFirstLetter = string =>
    string.charAt(0).toUpperCase() + string.slice(1);

const generateLinks = (service, env, domain) => {
    const output = [];

    const {
        frontPage,
        article,
        MAP,
        liveRadio
    } = services[service];

    if (frontPage && frontPage[env]) {
        output.push(`[frontPage](${domain}/${service})`);
    }

    if (article && article[env]) {
        output.push(`[article](${domain}/${service}/articles/${article[env]})`);
    }

    if (liveRadio && liveRadio[env]) {
        output.push(`[liveRadio](${domain}/${service}/${liveRadio[env]})`);
    }

    if (MAP && MAP[env]) {
        output.push(`[MAP](${domain}/${service}/${MAP[env]})`);
    }

    return output.join(' - ');
};

const generateLaunchDates = service => {
    const output = [];
    const {
        frontPage,
        article,
        MAP,
        liveRadio
    } = services[service];
    if (frontPage && frontPage.launch !== '') {
        output.push(`frontPage: ${frontPage.launch}`);
    }

    if (article && article.launch !== '') {
        output.push(`article: ${article.launch}`);
    }

    if (liveRadio && liveRadio.launch !== '') {
        output.push(`liveRadio: ${liveRadio.launch}`);
    }

    if (MAP && MAP.launch !== '') {
        output.push(`MAP: ${MAP.launch}`);
    }

    return output.join(' - ');
};

